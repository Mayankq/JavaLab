public class swap {
    public static void main(String[] args) {
        int a = 20, b = 30, c;
        System.out.println("The values of a and b before swapping are: " + a + " and " + b);
        c = a;
        a = b;
        b = c;
        System.out.println("The values of a and b before swapping are: " + a + " and " + b);
    }
}
